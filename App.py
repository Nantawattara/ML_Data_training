input_df = pd.DataFrame([[complain, age, is_active, num_products, geography, balance]], 
                        columns=["Complain", "Age", "IsActiveMember", "NumOfProducts", "Geography", "Balance"])

# ‡∏õ‡∏£‡∏±‡∏ö‡∏Ç‡∏ô‡∏≤‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏î‡πâ‡∏ß‡∏¢ StandardScaler
